func TestHBARule_String_Local(t *testing.T) {
	r := configfile.HBARule{
		Type:     "local",
		Database: "all",
		User:     "postgres",
		Method:   "peer",
	}
	s := r.String()
	if !strings.HasPrefix(s, "local") {
		t.Errorf("local rule should start with 'local': %q", s)
	}
	// Local rules must not contain an address field.
	parts := strings.Fields(s)
	if len(parts) != 4 {
		t.Errorf("local rule should have 4 fields, got %d: %q", len(parts), s)
	}
}

