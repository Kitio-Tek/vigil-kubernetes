func TestPostgreSQLConf_SortedOutput(t *testing.T) {
	params := map[string]string{
		"wal_level":        "replica",
		"listen_addresses": "*",
		"max_connections":  "200",
	}
	out := configfile.PostgreSQLConf(params)
	lines := strings.Split(strings.TrimSpace(out), "\n")

	// Find non-comment, non-blank lines.
	paramLines := make([]string, 0, len(lines))
	for _, l := range lines {
		if l == "" || strings.HasPrefix(l, "#") {
			continue
		}
		paramLines = append(paramLines, l)
	}
	if len(paramLines) != 3 {
		t.Fatalf("expected 3 param lines, got %d: %v", len(paramLines), paramLines)
	}
	// Should be alphabetically sorted.
	if !strings.HasPrefix(paramLines[0], "listen_addresses") {
		t.Errorf("first param should be listen_addresses, got: %q", paramLines[0])
	}
	if !strings.HasPrefix(paramLines[1], "max_connections") {
		t.Errorf("second param should be max_connections, got: %q", paramLines[1])
	}
	if !strings.HasPrefix(paramLines[2], "wal_level") {
		t.Errorf("third param should be wal_level, got: %q", paramLines[2])
	}
}

func TestPostgreSQLConf_QuotedValues(t *testing.T) {
	params := map[string]string{
		"archive_command": "cp %p /mnt/wal/%f",
		"max_connections": "100",
		"wal_level":       "replica",
	}
	out := configfile.PostgreSQLConf(params)

	if !strings.Contains(out, "archive_command = 'cp %p /mnt/wal/%f'") {
		t.Errorf("archive_command should be quoted, output:\n%s", out)
	}
	if !strings.Contains(out, "max_connections = 100") {
		t.Errorf("max_connections should not be quoted, output:\n%s", out)
	}
}

func TestPostgreSQLConf_BooleanNotQuoted(t *testing.T) {
	params := map[string]string{
		"hot_standby": "on",
		"ssl":         "off",
	}
	out := configfile.PostgreSQLConf(params)

	if strings.Contains(out, "'on'") || strings.Contains(out, "'off'") {
		t.Errorf("boolean values should not be quoted:\n%s", out)
	}
}

