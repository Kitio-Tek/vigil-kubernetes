func TestPgHBAConf_ContainsRules(t *testing.T) {
	rules := configfile.DefaultRules("10.0.0.0/8")
	out := configfile.PgHBAConf(rules)

	if !strings.Contains(out, "local") {
		t.Error("expected local rule in pg_hba.conf")
	}
	if !strings.Contains(out, "replication") {
		t.Error("expected replication rule in pg_hba.conf")
	}
	if !strings.Contains(out, "scram-sha-256") {
		t.Error("expected scram-sha-256 in pg_hba.conf")
	}
}

